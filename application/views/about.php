    <h1>Sobre 750 palabras</h1>
    <p>
        750 palabras está basado en la idea de Julia Cameron, que en su libro The Artist's way,
        proponía un sencillo ejercicio de escritura: las "Morning pages".
        <br /><br />
        La idea de aplicar este ejercicio a una aplicación web surgió inspirada por
        <a href="http://www.750words.com">750words.com</a>, web de la que he tomado la mayor parte
        de ideas para 750 palabras.
    </p>
    <p>
        750 palabras nació como un ejercicio para aprender a usar bases de datos NoSQL (MongoDB) mientras
        desarrollaba algo sencillo y útil. Como me gustó el resultado, lo he dejado abierto para que quien
        quiera pueda usarlo.
        <br /><br />
    </p>
    <h2>¿Quién está detrás de esto?</h2>
    <p>
        Soy Álvaro Remesal, un desarrollador web freelance al que le gusta jugar con nuevas tecnologías en
        el mundo del desarrollo web. Esta aplicación web es el resultado de aprender a usar MongoDB.
        <br /><br />
        Puedes leer más sobre mi en mi web: <a href="http://www.alvaroremesal.net">www.alvaroremesal.net</a>
        <br /><br />
    </p>
    <h2>Avisos legales</h2>
    <p>
        750 palabras es una aplicación personal que, si bien abierta al público en general, no ofrece ninguna
        garantía de continuidad. No me hago responsable de los datos introducidos en el sistema, más allá de mantener
        su seguridad y privacidad. 750 palabras es un juego, una aplicación desarrollada como ejercicio de estilo.
        750 palabras puede cerrar en cualquier momento, previo aviso en su portada. En todo momento el usuario
        tiene disponible la opción de obtener una copia de todos sus escritos en diversos formatos.
        <br /><br />
    </p>
