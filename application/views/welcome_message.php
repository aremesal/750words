<div id="dvWhatisthis">
    <h1>¿Qué es 750 palabras?</h1>
    <p>
        Julia Cameron, en su libro The Artist's way, proponía un sencillo ejercicio de escritura: las "Morning pages".
        El ejercicio consiste en escribir, a poder ser a primera hora de la mañana, tres páginas sobre cualquier cosa
        que te pase por la cabeza. La meta es crear el hábito de escribir estas tres páginas cada día, vaciando en ellas
        todo lo que tienes en mente de una forma cruda y directa. Son escritos que no se espera que sean editados, ni
        censurados (ni auto-censurados) de ninguna forma, ni leídos por nadie más. Este ejercicio liberará tu cabeza
        y permitirá que el resto del día seas más creativo.
        <br /><br />
    </p>
    <p>
        Este ejercicio es una forma de pensar en voz alta sin preocuparte por tus ideas a medias de pensar, por irte por
        las ramas o por tu privacidad. Es como un volcado de cerebro diario.
        <br /><br />
    </p>
    <h2>¿Porqué 750?</h2>
    <p>
        Se considera que una página son unas 250 palabras, por lo que la medida que proponía Julia Cameron en su libro
        serían estas 750 palabras. Por supuesto, eres libre de escribir más o menos cada día, según tu estado de ánimo.
        <br /><br />
    </p>
    <h2>¿Qué no es 750 palabras?</h2>
    <p>
        <b>No es un blog</b>
        <br />
        750 palabras es un lugar privado donde escribir, y no está pensado para ser visto públicamente, por lo que no
        es un blog ni nada similar. Lo que escribas sólo lo podrás ver tú.
        <br /><br />
        <b>No es diario online</b>
        <br />
        No se trata de una aplicación para llevar tu diario online, aunque lo puedes usar así si es lo que buscas.
        <br /><br />
    </p>
    <p>
        <b>Consejo: </b>para escribir sin distracciones, pulsa F11.
    </p>
